package co.develhope.Javadoc02;

/**
 * This class performs basic operations with integer using the following methods.
 * @author Luca Di Leo
 */
public class ArithmeticOperators {

    /**
     * This method make sum between the two arguments.
     * @param num1 is an int.
     * @param num2 is an int.
     * @return an int.
     */
    public int sum(int num1, int num2){
        return num1 + num2;
    }

    /**
     * This method make the subtraction between the two arguments.
     * @param num1 is an int.
     * @param num2 is an int.
     * @return an int.
     */
    public int sub(int num1, int num2){
        return num1 - num2;
    }

    /**
     * This method make the multiplication between the two arguments.
     * @param num1 is an int.
     * @param num2 is an int.
     * @return an int.
     */
    public int mul(int num1, int num2){
        return num1 * num2;
    }

    /**
     * This method make division between the two arguments.
     * @param num1 is an int.
     * @param num2 is an int.
     * @return an int.
     */
    public int div(int num1, int num2){
        return num1 / num2;
    }
}
